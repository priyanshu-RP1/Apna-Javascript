//a compact way of making a functions 

// ✅ Define a compact arrow function named 'noob'
// It takes one parameter 'b' and logs a message to the console
var noob = (b) => {
    console.log(b, "is a noob");
};

// ✅ Declare a variable 'name' and assign it the string "ranjan"
var name = "ranjan";

// ✅ Call the 'noob' function with 'name' as the argument
noob(name); // Output: ranjan is a noob